#include "ft_printf.h"

int	ft_isalpha(int j)
{
	return ((j >= 'A' && j <= 'Z') || (j >= 'a' && j <= 'z'));
}

int	ft_isdigit(int j)
{
	return (j >= '0' && j <= '9');
}
